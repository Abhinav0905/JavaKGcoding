class HalfPyramidJava {
    public static void main(String[] args) {
        System.out.println("\t*");
        System.out.println("\t**");
        System.out.println("\t***");
        System.out.println("\t****");
        System.out.println("\t*****");
    }

    class pyramid {
        public void main(String[] args) {
            System.out.println("    *");
            System.out.println("   ***");
            System.out.println("  *****");
            System.out.println(" *******");
            System.out.println("*********");
        }

    }
}
